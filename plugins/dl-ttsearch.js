import fg from 'fg-senna'

let handler = async (m, { text }) => {
  // 1. التحقق من إدخال نص البحث: إذا لم يكتب المستخدم شيئاً يتم تنبيهه برمجياً
  if (!text) return m.reply('⚠️ يرجى كتابة كلمة أو عنوان للبحث عنه في تيك توك.')

  try {
    // 2. استدعاء دالة ttsearch من مكتبة fg-senna لتنفيذ عملية البحث السحابي
    const res = await fg.ttsearch(text)
    const results = res.result

    // التحقق مما إذا كانت نتائج البحث فارغة
    if (!results || results.length === 0) return m.reply('❌ لم يتم العثور على أي نتائج مطابقة لبحثك.')

    // 3. استخدام دالة الخريطة (.map) للمرور على النتائج وصياغتها باللغة العربية
    const teks = results.map((v, i) => {
      return `🎬 *النتيجة رقم* ${i + 1}
📌 *العنوان:* ${v.title || 'بدون عنوان'}
👤 *صانع المحتوى (الكاتب):* ${v.author || 'غير معروف'}
🔗 *رابط المقطع المباشر:* ${v.link || 'غير متوفر'}`
    }).join('\n━━━━━━━━━━━━━━\n')

    // 4. إرسال قائمة نتائج البحث المنسقة بالكامل إلى المحادثة
    m.reply(teks)

  } catch (err) {
    // التعامل الآمن مع الأخطاء في حال انقطاع اتصال الواجهة البرمجية بالخادم
    console.error(err)
    m.reply('❌ فشلت عملية البحث في تيك توك حالياً، يرجى المحاولة مرة أخرى لاحقاً.')
  }
}

// إعدادات المساعدة والتصنيف للأمر بعد تعريبها لسهولة الوصول من المنيو العربي
handler.help = ['بحث_تيكتوك']
handler.tags = ['التحري / الأدوات']

// مصفوفة الكلمات المفتاحية التي يستجيب لها البوت باللغتين العربية والإنجليزية
handler.command = ['بحث_تيك', 'بحث_تيكتوكFilter', 'تيك_سيرش', 'tiktoksearch', 'ttsearch', 'tks']

export default handler
