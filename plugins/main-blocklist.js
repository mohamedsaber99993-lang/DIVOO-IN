let handler = async (m, { conn, usedPrefix, command }) => {
	
    // 1. استدعاء مصفوفة الأرقام المحظورة مباشرة من خوادم واتساب
    await conn.fetchBlocklist().then(async data => {
        
        // صياغة واجهة التقرير باللغة العربية مع حساب إجمالي المحظورين تلقائياً
        let txt = `*≡ قائمة الأرقام والحسابات المحظورة*\n\n*الإجمالي:* [ ${data.length} ] حساب محظور\n\n┌─⊷\n`
		
        // حلقة التكرار لفرز واقتطاع المعرفات الرقمية الصافية للمحظورين
        for (let i of data) {
            txt += `▢ @${i.split("@")[0]}\n`
        }
        txt += "└───────────"
		
        // 2. إرسال القائمة النهائية مع حقن نظام المنشن الذكي لقراءة الإشارات
        return conn.reply(m.chat, txt, m, { mentions: await conn.parseMention(txt) })
        
    }).catch(err => {
        // إدارة الأخطاء في حال كانت القائمة فارغة تماماً ولا يوجد أي محظور
        console.log(err);
        throw "✳️ لا توجد أي حسابات أو أرقام في قائمة الحظر حالياً."
    })
}

// إعدادات المساعدة والتصنيف للأمر بعد تعريبها لسهولة الوصول من المنيو العربي
handler.help = ['قائمة_المحظورين']
handler.tags = ['أوامر المطور والمشرف']

// مصفوفة الكلمات المفتاحية والاختصارات للأمر باللغتين العربية والإنجليزية
handler.command = ['المحظورين', 'قائمة_الحظر', 'المبندين', 'blocklist', 'listblock'] 

export default handler
