let handler = m => m

// دالة الفحص المسبق (Listener) التي تعمل تلقائياً قبل تنفيذ الأوامر الأخرى
handler.before = async function (m) {

  // إذا لم تكن الرسالة تحتوي على نص، أو لم تكن رقماً صريحاً، يتم تجاهلها وتمريرها بأمان
  if (!m.text) return !0
  if (isNaN(m.text)) return !0

  this.math = this.math ? this.math : {}
  let id = m.chat

  // التحقق من وجود لعبة حسابية نشطة حالياً في هذه المجموعة؛ إذا لم توجد يتم تجاهل النص
  if (!(id in this.math)) return !0

  let mathData = this.math[id]
  let math = mathData[1]

  // 🔒 حماية برمجية: لا يتم قبول الإجابة إلا إذا قام اللاعب بالرد (Quote) على رسالة السؤال نفسها
  if (!m.quoted) return !0
  if (m.quoted.id !== mathData[0].id) return !0

  // حماية قاعدة البيانات: التأكد من تسجيل حساب المستخدم وتوليد مصفوفته إذا كان جديداً
  if (!global.db.data.users[m.sender])
    global.db.data.users[m.sender] = { coin: 0 }

  let user = global.db.data.users[m.sender]
  if (!user.coin) user.coin = 0

  // ──────────────────────────────────────────────────────────
  // [الحالة الأولى: إذا كانت الإجابة المدخلة صحيحة تماماً]
  // ──────────────────────────────────────────────────────────
  if (Number(m.text) === Number(math.result)) {

    // إضافة الجائزة المالية المحددة مسبقاً إلى محفظة اللاعب الكاش
    user.coin += math.bonus

    clearTimeout(mathData[3]) // إيقاف وإلغاء مؤقت انتهاء الوقت التنازلي فوراً لمنع حدوث تعارض
    delete this.math[id]     // مسح جلسة اللعبة الحالية من الذاكرة العشوائية بنجاح

    // إرسال رسالة التهنئة باللغة العربية مدمجة مع قيمة الجائزة
    m.reply(`✅ *إجابة صحيحة وممتازة!*

🎁 لقد ربحت: *+${math.bonus} 🪙* من العملات النقدية!`)

  } else {
    // ──────────────────────────────────────────────────────────
    // [الحالة الثانية: إذا كانت الإجابة خاطئة]
    // ──────────────────────────────────────────────────────────
    mathData[2]-- // خصم محاولة واحدة من رصيد محاولات المجموعة (الافتراضي 3 محاولات)

    // إذا نفدت جميع المحاولات المتاحة للحل
    if (mathData[2] <= 0) {

      clearTimeout(mathData[3]) // إلغاء المؤقت التنازلي
      delete this.math[id]     // إغلاق اللعبة ومسح الجلسة

      // إرسال رسالة الخسارة وكشف الحل الصحيح للأعضاء
      m.reply(`❌ *للأسف، نفدت جميع المحاولات المتاحة للحل!*

📌 الإجابة الصحيحة كانت: *${math.result}*`)

    } else {
      // إذا كانت هناك محاولات متبقية، يتم إشعارهم بالعدد المتبقي
      m.reply(`❎ *إجابة خاطئة!*

🔄 حاول مجدداً، متبقٍ للمجموعة: *${mathData[2]} محاولات*`)
    }
  }

  return !0
}

export default handler
