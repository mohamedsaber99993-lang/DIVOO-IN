//import db from '../lib/database.js'

let handler = async (m, { conn, text, isROwner, isOwner, usedPrefix, command }) => {
  
  // 1. إذا قام المشرف بكتابة نص رسالة الترحيب الجديدة
  if (text) {
    // حفظ النص المخصص في قاعدة بيانات الدردشة للجروب الحالي
    global.db.data.chats[m.chat].sWelcome = text
    
    // إشعار تأكيد النجاح باللغة العربية
    m.reply(`✅ تم بنجاح حفظ وتعيين رسالة الترحيب المخصصة لجميع الأعضاء الجدد في هذا الجروب.`)
    
  } else {
    // 2. إذا لم يكتب نصاً، يتم إلقاء استثناء يوضح طريقة الاستخدام والدعم البرمجي للمتغيرات
    throw `✳️ يرجى إدخال نص رسالة الترحيب المطلوبة بعد الأمر.

📌 *المتغيرات المتاحة للاستخدام:*
• *@user* ← (عمل منشن للعضو الجديد تلقائياً)
• *@group* ← (عرض اسم المجموعة الحالي)
• *@desc* ← (عرض وصف وشروط المجموعة)

📌 *مثال على الاستخدام:*
*${usedPrefix + command}* أهلاً بك يا @user في مجموعة @group، يرجى قراءة الوصف: @desc`
  }
}

// إعدادات المساعدة والتصنيف للأمر بعد تعريبها لسهولة الوصول من المنيو العربي
handler.help = ['تعيين_الترحيب [النص]']
handler.tags = ['إشراف المجموعات']

// مصفوفة الكلمات المفتاحية والاختصارات للأمر باللغتين العربية والإنجليزية
handler.command = ['تعيين_الترحيب', 'رسالة_الترحيب', 'setwelcome'] 

// أقفال الحماية والصلابة البرمجية للأمر
handler.admin = true   // قفل حماية: يسمح للمشرفين فقط بالتحكم في رسائل النظام للجروب
handler.owner = false  // لا يشترط أن يكون مالك البوت الأساسي لتخفيف القيود عن المشرفين

export default handler
