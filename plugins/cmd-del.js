let handler = async (m, { text }) => {
    let hash = text // تعيين النص المدخل كبصمة (إذا تم إدخال البصمة يدوياً كـ Hash)
    
    // إذا قام المطور بالرد (Quoted) على الملصق المراد حذفه، يتم استخراج البصمة الرقمية له وتحويلها إلى نظام سداسي عشر (Hex)
    if (m.quoted && m.quoted.fileSha256) hash = m.quoted.fileSha256.toString('hex')
    
    // إذا لم يتم كتابة بصمة ولم يتم الرد على ملصق، يطلب البوت تحديد اسم أو الرد على الملصق
    if (!hash) throw `✳️ يرجى الرد على الملصق المراد حذف الأمر المرتبط به.`
    
    let sticker = global.db.data.sticker
    
    // التحقق مما إذا كان الأمر محمياً أو مغلقاً (Locked) من قِبل النظام لتفادي حذفه بالخطأ
    if (sticker[hash] && sticker[hash].locked) throw '✳️ لا يمكنك حذف هذا الأمر، إنه محمي ومغلق من قِبل النظام.'
    
    // حذف بصمة الملصق والأمر المرتبط بها نهائياً من قاعدة بيانات الملصقات
    delete sticker[hash]
    
    // إرسال رسالة تأكيد النجاح باللغة العربية
    m.reply(`✅ تم حذف الأمر المرتبط بالملصق بنجاح.`)
}

// إعدادات المساعدة والتصنيف للأمر بعد تعريبها
handler.help = ['حذف_امر_الملصق']
handler.tags = ['التحكم / الأوامر']
handler.command = ['حذف_امر_الملصق', 'حذف_امر_ملصق', 'delcmd']

// تفعيل قيد المالك الصارم: لضمان أن مطور البوت الأساسي فقط هو من يستطيع تعديل قاعدة بيانات الأوامر
handler.rowner = true

export default handler
