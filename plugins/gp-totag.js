let handler = async (m, { conn, text, participants }) => {
	
    // 1. استخراج وفلترة معرفات جميع الأعضاء الحاليين في المجموعة (مع استثناء البوت)
    let users = participants.map(u => u.id).filter(v => v !== conn.user.jid)
    
    // التحقق الاستباقي: إذا لم يقم المشرف بالرد على الرسالة المراد عمل تاغ لها
    if (!m.quoted) throw `✳️ يرجى الرد (الامتثال) مباشرة على الرسالة التي ترغب في عمل تاغ جماعي لها.`
    
    // 2. إعادة توجيه كائن الرسالة المقتبسة وحقن مصفوفة الأعضاء في قائمة الإشارات
    conn.sendMessage(m.chat, { forward: m.quoted.fakeObj, mentions: users } )
}

// إعدادات المساعدة والتصنيف للأمر بعد تعريبها لسهولة الوصول من المنيو العربي
handler.help = ['تاغ_للرسالة']
handler.tags = ['إشراف المجموعات']

// التعبير النمطي للمصفوفة البرمجية لدعم الكلمات العربية والإنجليزية
handler.command = /^(totag|tag|تاغ|اشارة|منشن_رسالة)$/i

// أقفال الحماية والصلابة البرمجية للأمر
handler.admin = true  // قفل حماية: يسمح للمشرفين فقط باستخدام الأمر لتفادي الإزعاج
handler.group = true  // يعمل هذا الأمر داخل المجموعات فقط لارتباطه بمصفوفة الأعضاء

export default handler
