let handler = async (m, { conn, args }) => {
  // 1. تحويل كائن بيانات المستخدمين من قاعدة البيانات إلى مصفوفة قابلة للتكرار (Array)
  let list = Object.entries(global.db.data.users)
  
  // 2. استخدام حلقة تكرارية (forEach) للمرور على حساب كل مستخدم وتصفير خصائصه الاقتصادية
  list.forEach(([user, data]) => {
    data.diamond = Number(20)       // إعادة رصيد الألماس الافتراضي إلى 20 ماسة لكل لاعب
    data.coin = Number(0)          // تصفير العملات النقدية في المحفظة
    data.bank = Number(0)          // تصفير الحسابات البنكية المودعة
    data.crime = Number(0)         // تصفير سجل الجرائم أو النقاط السوداء
    data.lastmiming = Number(0)     // تصفير مؤقت انتظار التعدين (إتاحة اللعب الفوري)
    data.lastwork = Number(0)       // تصفير مؤقت انتظار العمل
    data.lastclaim = Number(0)      // تصفير مؤقت انتظار المكافأة اليومية
    data.weekly = Number(0)         // تصفير مؤقت انتظار المكافأة الأسبوعية
    
    // ملاحظة برمجية: تم إبقاء مستويات المستخدمين ونقاط الخبرة (XP) دون تصفير
    // data.level = Number(0)
    // data.exp = Number(0)
  })
  
  // 3. إرسال رسالة تأكيد إتمام تصفير النظام بنجاح باللغة العربية
  m.reply(`✅ تم بنجاح إعادة تعيين وتصفير اقتصاد البوت بالكامل لجميع المستخدمين.`);
}

// مصفوفة الكلمات المفتاحية والاختصارات للأمر باللغتين العربية والإنجليزية
handler.command = /^(resetbalance|resetbal|reseteconomia|تصفير_الاقتصاد|تصفير)$/i

// قفل حماية صارم: لضمان أن مطور البوت الأساسي فقط من يمتلك صلاحية مسح البيانات
handler.rowner = true

export default handler
