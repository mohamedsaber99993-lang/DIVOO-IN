let handler = async (m, { conn }) => {

    // 1. جلب كائن إحصائيات الرسائل من قاعدة البيانات العامة، أو تهيئته ككائن فارغ
    let stats = global.db.data.statsMsg || {}
    
    // التحقق الاستباقي: إذا لم تكن هناك أي بيانات أو عداد مسجل لهذه المجموعة مسبقاً
    if (!stats[m.chat]) {
        return m.reply('⚠️ لا توجد بيانات عداد مسجلة في هذه المجموعة لتصفيرها.')
    }

    // 2. تدمير وحذف بيانات عداد الرسائل الخاصة بالمجموعة الحالية من الذاكرة
    delete stats[m.chat]

    // تحديث قاعدة البيانات العامة بالقيم الجديدة بعد الحذف
    global.db.data.statsMsg = stats

    // 3. إرسال إشعار تأكيد النجاح باللغة العربية
    m.reply('🧹 *تم بنجاح تصفير وإعادة تعيين عداد الرسائل لجميع الأعضاء في هذه المجموعة.*')
}

// إعدادات المساعدة والتصنيف للأمر بعد تعريبها لسهولة الوصول من المنيو العربي
handler.help = ['تصفير_العداد']
handler.tags = ['إشراف المجموعات']

// مصفوفة الكلمات المفتاحية والاختصارات للأمر باللغتين العربية والإنجليزية
handler.command = ['تصفير_العداد', 'تصفير_الرسائل', 'resetmsg']

// أقفال الحماية والصلابة البرمجية للأمر
handler.group = true // يعمل هذا الأمر داخل المجموعات (الجروبات) فقط
handler.admin = true // قفل حماية: يسمح للمشرفين فقط بصلاحية تصفير الإحصائيات

export default handler
