import { createHash } from 'crypto'

let handler = async function (m, { conn, args, usedPrefix}) {
  
  // 1. التحقق الاستباقي: إذا لم يقم المستخدم بكتابة الرمز التسلسلي الخاص به مع الأمر
  if (!args[0]) throw `✳️ يرجى إدخال الرمز التسلسلي الخاص بك لإتمام العملية.\n\nيمكنك معرفة رمزك الشخصي باستخدام الأمر:\n*${usedPrefix}الرقم_التسلسلي*`
  
  let user = global.db.data.users[m.sender]
  
  // توليد الرمز التسلسلي الخاص بمرسل الرسالة للتحقق من هويته
  const sn = createHash('md5').update(m.sender).digest('base64').replace(/[^a-zA-Z0-9]/g, '').slice(0, 8)
  
  // 2. قفل الأمان: التأكد من مطابقة الرمز المدخل مع الرمز الصحيح للمستخدم
  if (args[0] !== sn) throw `⚠️ *الرمز التسلسلي الذي أدخلته غير صحيح!*`
  
  // 3. إعادة تعيين وتصفير بيانات التسجيل والجنس في قاعدة البيانات
  user.registered = false
  user.rgenero = false
  
  // 4. إرسال رسالة التأكيد النهائية باللغة العربية مع قالب الرسائل المتقدم (fwc)
  m.reply(`✅ تم إلغاء تسجيل حسابك وحذف ملفك التعريفي بنجاح من قاعدة البيانات.`, null, fwc)
}

// إعدادات المساعدة والتصنيف للأمر بعد تعريبها لسهولة الوصول من المنيو العربي
handler.help = ['إلغاء_التسجيل <الرمز_التسلسلي>'] 
handler.tags = ['الرئيسية والمعلومات']

// مصفوفة الكلمات المفتاحية والاختصارات للأمر باللغتين العربية والإنجليزية
handler.command = ['unreg', 'الغاء_التسجيل', 'حذف_حسابي', 'الغاء_تسجيل'] 

// تفعيل قفل التسجيل (يجب أن يكون المستخدم مسجلاً بالفعل ليتمكن من استخدام أمر إلغاء التسجيل)
handler.register = true

export default handler
