let handler = async function (m, { conn, text, usedPrefix, command }) {
	
  // جلب كائن قاعدة البيانات الخاص بالمجموعة الحالية
  let chat = global.db.data.chats[m.chat]
  
  // 1. إذا قام المشرف بكتابة نص القوانين الجديد بعد الأمر
  if (text) {
    chat.rules = text // حفظ وتحديث نص القوانين في قاعدة البيانات
    
    // إشعار تأكيد النجاح باللغة العربية
    m.reply(`✅ تم بنجاح حفظ وتعيين *قوانين المجموعة* الجديدة.`)
    
  } else {
    // 2. إذا لم يكتب أي نص، يتم إلقاء استثناء يوضح طريقة الاستخدام الصحيحة
    throw `✳️ يرجى إدخال نص قوانين المجموعة المراد تعيينها.\n\n📌 *مثال:* \n*${usedPrefix + command}* 1. ممنوع الروابط\n2. احترم الأعضاء.`
  }
     
} 

// إعدادات المساعدة والتصنيف للأمر بعد تعريبها لسهولة الوصول من المنيو العربي
handler.help = ['تعيين_القوانين [النص]']
handler.tags = ['إشراف المجموعات']

// مصفوفة الكلمات المفتاحية والاختصارات للأمر باللغتين العربية والإنجليزية
handler.command = ['تعيين_القوانين', 'اضف_قوانين', 'setrules', 'addrules', 'addrule'] 

// أقفال الحماية والصلابة البرمجية للأمر
handler.group = true // يعمل هذا الأمر داخل المجموعات (الجروبات) فقط لحماية كائن chat
handler.admin = true // قفل حماية: يسمح فقط للمشرفين بصلاحية صياغة وقفل قوانين الجروب

export default handler
