let handler = async function (m, { conn, text, usedPrefix, command }) {
	
    // 1. جلب بيانات كائن المجموعة الحالية من قاعدة البيانات العامة
    let chat = global.db.data.chats[m.chat]
    
    // التحقق الاستباقي: إذا كانت خانة القوانين فارغة ولم يتم ضبطها من قبل المشرفين بعد
    if (!chat.rules || chat.rules === '') {
        throw `✳️ عذراً، هذه المجموعة لا تحتوي على قوانين أو شروط مسجلة في الوقت الحالي.`
    }
    
    // 2. إرسال قوانين المجموعة المنسقة باللغة العربية مع قالب اقتباس الرسائل المتقدم (fwc)
    m.reply(`📜 *قوانين وشروط المجموعة*\n\n${chat.rules}`, null, fwc)
     
}

// إعدادات المساعدة والتصنيف للأمر بعد تعريبها لسهولة الوصول من المنيو العربي
handler.help = ['القوانين']
handler.tags = ['إشراف المجموعات']

// مصفوفة الكلمات المفتاحية والاختصارات للأمر باللغتين العربية والإنجليزية
handler.command = ['قوانين', 'القوانين', 'الشروط', 'الرولز', 'rules', 'reglas'] 

export default handler
