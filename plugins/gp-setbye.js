let handler = async (m, { conn, text, isROwner, isOwner, usedPrefix, command }) => {
  
  // 1. إذا قام المشرف بكتابة نص رسالة المغادرة الجديدة
  if (text) {
    // حفظ النص المخصص في قاعدة بيانات الدردشة للجروب الحالي
    global.db.data.chats[m.chat].sBye = text
    
    // إشعار تأكيد النجاح باللغة العربية
    m.reply(`✅ تم بنجاح حفظ وتعيين رسالة الوداع والمغادرة المخصصة لهذا الجروب.`)
    
  } else {
    // 2. إذا لم يكتب نصاً، يتم إلقاء استثناء يوضح طريقة الاستخدام والدعم البرمجي
    throw `✳️ يرجى إدخال نص رسالة الوداع والمغادرة المطلوبة.\n\n📌 *مثال:* \n*${usedPrefix + command}* مع السلامة يا @user نورتنا!`
  }
}

// إعدادات المساعدة والتصنيف للأمر بعد تعريبها لسهولة الوصول من المنيو العربي
handler.help = ['تعيين_الوداع [النص]']
handler.tags = ['إشراف المجموعات']

// مصفوفة الكلمات المفتاحية والاختصارات للأمر باللغتين العربية والإنجليزية
handler.command = ['تعيين_الوداع', 'رسالة_المغادرة', 'setbye'] 

// أقفال الحماية والصلابة البرمجية للأمر
handler.admin = true   // قفل حماية: يسمح للمشرفين فقط بالتحكم في رسائل النظام للجروب
handler.owner = false  // لا يشترط أن يكون مالك البوت الأساسي لتخفيف القيود

export default handler
