let handler = async (m, { conn, text, usedPrefix, command }) => {
    
    // دالة برمجية داخلية لتنظيف المدخلات (حذف الفراغات، الرموز، وعلامات الزائد والمنشن)
    function no(number){
        return number.replace(/\s/g,'').replace(/([@+-])/g,'')
    }
 
    text = no(text) // معالجة النص المدخل وتنظيفه
    
    // التحقق من نوع المدخلات لتحديد ما إذا كان النص يحتوي على معرف أو رقم هاتف صريح
    if (isNaN(text)) {
        var number = text.split`@`[1]
    } else if (!isNaN(text)) {
        var number = text
    }

    // 1. التحقق من وجود مستهدف: إذا لم يتم إدخال نص ولم يتم الرد على رسالة، يطلب البوت تحديد المستخدم
    if (!text && !m.quoted) return m.reply(`✳️ يرجى تحديد المستخدم عن طريق عمل منشن (@) أو الرد على رسالته أو كتابة رقمه.`)
    
    // 2. التحقق من صحة الرقم المستخرج برمجياً
    if (isNaN(number) && !m.quoted) return m.reply(`✳️ خطأ: القيمة المخرجة أو رقم الهاتف الذي أدخلته غير صالح.`)

    try {
        // 3. هندسة جلب المعرف الموحد (JID) للمستخدم بناءً على طريقة الاستدعاء
        if (text) {
            var user = number + '@s.whatsapp.net' // إذا تم إدخال رقم هاتف صريح
        } else if (m.quoted.sender) {
            var user = m.quoted.sender          // إذا تم الرد على رسالة المستخدم
        } else if (m.mentionedJid) {
            var user = number + '@s.whatsapp.net' // إذا تم الإشارة إليه بالمنشن
        }  
    } catch (e) {
        // تجاهل الأخطاء العابرة أثناء محاولة الفرز الأمن للمعرفات
    } finally {
        // 4. تنفيذ العملية النهائية (Finally Block): لضمان التعديل على قاعدة البيانات وإرسال التنبيه
        let number = user.split('@')[0] // استخراج الرقم الصافي للعرض
        let num = global.db.data.users[user] // جلب ملف المستخدم من قاعدة البيانات
        
        // التحقق من وجود الحساب في قاعدة البيانات لتفادي الانهيار البرمجي لقاعدة البيانات
        if (!num) return m.reply(`✳️ هذا المستخدم غير مسجل في قاعدة بيانات البوت بعد.`)
        
        num.diamond = 8 // إعادة تعيين رصيد الألماس إلى القيمة الافتراضية المحددة (8 ماسات)
        
        // إرسال رسالة تأكيد النجاح باللغة العربية مع عمل منشن للحساب
        conn.reply(m.chat, `✅ تم بنجاح إعادة تعيين رصيد الألماس (Diamonds) إلى القيمة الافتراضية للمستخدم: @${number}`, m, { mentions: [user] })
    }
    
}

// الكلمة المفتاحية للأمر
handler.command = ['تصفير_الماس', 'تصفير_الألماس', 'reset-di'] 

// قفل حماية صارم: لضمان أن مطور البوت الأساسي فقط من يمتلك صلاحية تعديل حسابات الأفراد
handler.rowner = true

export default handler
