let handler = async (m, { conn, args, usedPrefix, command }) => {

  // 1. التحقق الاستباقي: إذا لم يكتب المشرف رمز الدولة أو لم يكن رقماً صحيحاً
  if (!args[0] || isNaN(args[0]))
    return m.reply(`✳️ يرجى إدخال رمز دولة (مقدمة رقم) صحيح وصالح.\n\n📌 مثال: *${usedPrefix + command}* 966`)

  // إزالة علامة (+) تلقائياً إذا قام المشرف بكتابتها لضمان سلامة الفحص البرمجي
  let prefix = args[0].replace(/\+/g, '')

  let users = []

  // 2. حلقة فحص وتصفية جميع أعضاء المجموعة حالياً
  for (let p of participants) {

    // قراءة الرقم الهاتفي الصافي للعضو إن وجد، أو استخدام المعرف الافتراضي
    let jid = p.phoneNumber || p.id
    if (!jid) continue

    // استثناء حساب البوت نفسه تلقائياً لمنع إدراج نفسه في قائمة الفرز
    if (jid === conn.user.jid) continue

    // اقتطاع الجزء الرقمي الصافي قبل علامة السيرفر
    let number = jid.split('@')[0]

    // التأكد من أن المعرف يتكون من أرقام صالحة فقط
    if (!/^\d+$/.test(number)) continue

    // إذا كان رقم العضو يبدأ برمز الدولة المحدد، يتم إضافته إلى مصفوفة النتائج
    if (number.startsWith(prefix)) {
      users.push(jid)
    }
  }

  // 3. التحقق من النتائج: إذا لم يتم العثور على أي عضو يحمل هذا الرمز
  if (!users.length)
    return m.reply(`✳️ المجموعة لا تحتوي على أي أعضاء يبدأ رقمهم بالرمز: +${prefix}`)

  // 4. صياغة واجهة العرض والتقرير النهائي باللغة العربية
  let text = `≡ *قائمة الأعضاء الذين يحملون الرمز:* +${prefix}\n\n`
  text += `👥 *إجمالي الأعضاء المكتشفين:* [ ${users.length} ] عضو.\n\n`

  // تكرار الأسماء وعمل التايج النصي لهم
  for (let jid of users) {
    let number = jid.split('@')[0]
    text += `▢ @${number}\n`
  }

  // إرسال التقرير النهائي إلى المجموعة مع تفعيل حقن المنشن لضمان وصول الإشعارات لهم
  await conn.sendMessage(m.chat, {
    text,
    mentions: users
  }, { quoted: m })
}

// إعدادات المساعدة والتصنيف للأمر بعد تعريبها لسهولة الوصول من المنيو العربي
handler.help = ['فرز_مقدمة [كود_الدولة]']
handler.tags = ['إشراف المجموعات']

// مصفوفة الكلمات المفتاحية والاختصارات للأمر باللغتين العربية والإنجليزية
handler.command = ['رقم', 'ارقام', 'فرز_دول', 'num']

// أقفال الحماية والصلابة البرمجية
handler.admin = true // قفل حماية: يسمح للمشرفين فقط باستخدام هذا الأمر الإحصائي

export default handler
