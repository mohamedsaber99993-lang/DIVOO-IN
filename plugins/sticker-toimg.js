import sharp from 'sharp'

let handler = async (m, { conn }) => {

  // 1. التحقق الاستباقي: التأكد من أن المستخدم قام بالرد على رسالة
  if (!m.quoted) throw '✳️ يرجى الرد على الملصق المراد تحويله إلى صورة.'

  let q = m.quoted
  // 2. التأكد من أن الرسالة التي تم الرد عليها هي ملصق بالفعل وليست شيئاً آخر
  if (!/sticker/.test(q.mediaType)) throw '✳️ عذراً، هذا الأمر يعمل فقط عند الرد على الملصقات.'

  // بدء تحميل ملف الملصق من سيرفرات واتساب إلى ذاكرة السيرفر المؤقتة
  let media = await q.download()
  if (!media) throw '❌ فشل تحميل الملصق، يرجى المحاولة مرة أخرى.'

  // 3. معالجة الملصق برمجياً عبر مكتبة sharp وتحويل امتداده إلى صورة PNG بدقة عالية
  let img = await sharp(media)
    .png()
    .toBuffer()

  // 4. إرسال ملف الصورة الناتج إلى الدردشة مع نص التأكيد باللغة العربية
  await conn.sendFile(
    m.chat, img, 'imagen.png', '✅ تم تحويل الملصق إلى صورة بنجاح.', m)

}

// إعدادات المساعدة والتصنيف للأمر بعد تعريبها لسهولة الوصول من المنيو العربي
handler.help = ['تحويل_لصورة']
handler.tags = ['قسم الملصقات (Stickers)']

// مصفوفة الكلمات المفتاحية والاختصارات للأمر باللغتين العربية والإنجليزية
handler.command = ['toimg', 'jpg', 'aimg', 'لصورة', 'تحويل_صورة', 'صورة']

export default handler
