let handler = async (m, { conn, usedPrefix, command }) => {
	
  // 1. التحقق الاستباقي: إذا لم يقم المشرف بعمل منشن للعضو ولم يقم بالرد على رسالته
  if (!m.mentionedJid[0] && !m.quoted) {
    return m.reply(`✳️ طريقة استخدام الأمر:\n\n*${usedPrefix + command}* @المنشن`) 
  }

  // تحديد هوية المستخدم المستهدف بالطرد (إما من المنشن المباشر أو صاحب الرسالة المقتبسة)
  let user = m.mentionedJid[0] ? m.mentionedJid[0] : m.quoted.sender

  // 2. حماية برمجية ذاتية: منع البوت من طرد نفسه من المجموعة لتفادي ثغرات التلاعب بالملفات
  if (conn.user.jid.includes(user)) {
    return m.reply(`✳️ خطأ أمني: لا يمكنني طرد نفسي من المجموعة برمجياً.`)
  }

  // 3. إرسال أمر الطرد الرسمي إلى خوادم واتساب لإخراج العضو فوراً
  await conn.groupParticipantsUpdate(m.chat, [user], 'remove')
  
  // 4. إشعار تأكيد النجاح باللغة العربية
  m.reply(`✅ تم بنجاح إخراج العضو وإزالته من المجموعة.`) 

}

// إعدادات المساعدة والتصنيف للأمر بعد تعريبها لسهولة الوصول من المنيو العربي
handler.help = ['طرد @العضو']
handler.tags = ['إشراف المجموعات']

// مصفوفة الكلمات المفتاحية والاختصارات للأمر باللغتين العربية والإنجليزية
handler.command = ['طرد', 'احذف', 'ازالة', 'إزالة', 'kick', 'expulsar'] 

// أقفال الحماية والصلابة البرمجية للأمر
handler.admin = true      // قفل حماية: يشترط أن يكون مرسل الأمر مشرفاً في المجموعة
handler.group = true      // يعمل هذا الأمر داخل المجموعات (الجروبات) فقط
handler.botAdmin = true   // قفل حماية: يشترط أن يكون البوت مشرفاً ليمتلك صلاحية إدارة الأعضاء وطردهم

export default handler
