let handler = async (m, { conn, text, usedPrefix, command, args }) => {
 
  // 1. التحقق من إدخال البيانات: إذا لم يكتب المستخدم شيئاً يتم توجيهه للمثال الصحيح
  if (!text) throw `✳️ كم عدد *العملات (Coins)* التي ترغب في سحبها من البنك؟\n\n📌 مثال: *${usedPrefix + command}* 500\nمثال لسحب الكل: *${usedPrefix + command}* all`

  let user = global.db.data.users[m.sender] // جلب بيانات حساب المستخدم من قاعدة البيانات
  
  // 2. التحقق من صحة المدخلات: يجب أن تكون الكمية رقماً صحيحاً موجباً أو كلمة 'all'
  if (args[0].toLowerCase() !== 'all' && !/^[1-9]\d*$/.test(args[0])) throw `✳️ خطأ: الكمية المحددة يجب أن تكون رقماً صالحاً وصحيحاً.`
  
  // جلب الرصيد الإجمالي الحالي للمطلب البنكي للمستخدم
  let all = Math.floor(global.db.data.users[m.sender].bank)
  
  // استبدال كلمة 'all' بالقيمة الإجمالية للرصيد البنكي إن وجدت، وضمان ألا تقل القيمة عن 1 برمجياً
  let count = args[0].replace('all', all)
  count = Math.max(1, count)

  if (isNaN(count)) throw `✳️ خطأ: الكمية المحددة يجب أن تكون رقماً صالحاً.`
  
  // الحماية المصرفية: منع المستخدم من سحب مبلغ أكبر مما يملكه في حسابه البنكي بالفعل
  if (count > user.bank) throw `✳️ عذراً، لا يمكنك سحب مبلغ أكبر مما تملكه في حسابك البنكي حالياً.`

  // 3. معالجة العملية: خصم النقاط من البنك وإضافتها إلى رصيد المحفظة الكاش
  user.bank -= count
  user.coin += count

  // 4. إرسال رسالة تأكيد النجاح باللغة العربية مع تنسيق الأرقام
  m.reply(`✅ تم بنجاح سحب *${count.toLocaleString()} 🪙* من حسابك البنكي وإعادتها لمحفظتك.`, null, fwc)
  
}

// إعدادات المساعدة والتصنيف للأمر بعد تعريبها لسهولة الوصول من المنيو العربي
handler.help = ['سحب']
handler.tags = ['الاقتصاد / العاب RPG']

// مصفوفة الاختصارات والكلمات المفتاحية للأمر باللغتين العربية والإنجليزية
handler.command = ['سحب', 'اسحب', 'withdraw', 'wd', 'retirar']

export default handler
