import axios from 'axios'

// كائن برمجى لتخزين سياق المحادثات لكل مستخدم للحفاظ على الذاكرة
let chats = {}

let handler = async (m, { conn, text, usedPrefix, command }) => {
  
  // 1. أمر إعادة تعيين الذاكرة (Reset AI Memory)
  if (command === 'resetai' || command === 'تصفير') {
    delete chats[m.sender]
    return m.reply('🧠 تم إعادة تعيين الذاكرة بنجاح والبدء من جديد.')
  }

  // 2. التحقق الاستباقي: إذا لم يقم المستخدم بكتابة سؤال أو نص للذكاء الاصطناعي
  if (!text) return m.reply(`✳️ طريقة استخدام الأمر:\n\n*${usedPrefix + command}* نص السؤال الخاص بك`)

  try {
    // وضع تفاعل المحادثة التلقائي (إيموجي صندوق المحادثة)
    m.react("💬")
    
    // استدعاء دالة الذكاء الاصطناعي وجلب الإجابة
    let res = await gpt(m.sender, text)

    // إرسال الإجابة للمستخدم مع قالب الرسائل المتقدم (fwc)
    m.reply(res, null, fwc)
    

  } catch (e) {
    m.reply('❎ خطأ: تعذر الاتصال بالذكاء الاصطناعي، يرجى المحاولة لاحقاً.')
  }

}

// إعدادات المساعدة والتصنيف للأمر بعد تعريبها لسهولة الوصول من المنيو العربي
handler.help = ['ذكاء <النص>', 'تصفير <لحذف ذاكرة المحادثة>']
handler.tags = ['أدوات ومساعدة']

// مصفوفة الكلمات المفتاحية والاختصارات للأمر باللغتين العربية والإنجليزية
handler.command = ["ia", "ai", "resetai", "gpt", "openai", "senna", "ذكاء", "سؤال", "بوت", "تصفير"]

export default handler


// الدالة البرمجية المسؤولة عن إدارة المحادثة والاتصال بـ API الذكاء الاصطناعي
async function gpt(userId, text) {

  // التوجيه البرمجي المخصص لتحديد شخصية وقوانين ردود البوت (System Prompt)
  let _systemPrompt = `أنت "سينا بوت" (Senna Bot)، مساعد ذكي، سريع ومفيد جداً.
تم تطويرك كبوت لخدمة تطبيق واتساب بواسطة FG، وتعتمد في تشغيلك على نموذج لغوي متطور.

يجب أن تجيب بشكل واضح، مختصر ومباشر، مع إضفاء لمسة لطيفة من الشخصية الودودة.
استخدم تنسيقات الماركداون (Markdown) لتحسين قراءة النصوص عند الحاجة.

القواعد الصارمة:
- لا تعطِ إجابات طويلة بشكل غير ضروري.
- كن دقيقاً، محدداً ومفيداً.
- إذا كان سؤال المستخدم غير مفهوم أو غامض، اطلب منه التوضيح بلطف.
- يمكنك استخدام دعابة خفيفة عندما يكون ذلك مناسباً وفي السياق.
- اجعل الأولوية القصوى دائماً هي تقديم المساعدة الحقيقية للمستخدم.`

  // إنشاء سجل محادثة جديد للمستخدم إذا لم يكن لديه سجل سابق وحقن توجيه النظام فيه
  if (!chats[userId]) {
    chats[userId] = [
      {
        role: "system",
        content: _systemPrompt
      }
    ]
  }

  // إضافة سؤال المستخدم الحالي إلى سجل الذاكرة
  chats[userId].push({
    role: "user",
    content: text
  })

  // --- إدارة وحجم الذاكرة المستهلكة ---
  let MAX_MESSAGES = 12

  // إذا تجاوزت المحادثة 12 رسالة، يتم الاحتفاظ بتوجيه النظام الأساسي مع آخر 10 رسائل فقط لتجنب بطء الاستجابة
  if (chats[userId].length > MAX_MESSAGES) {
    chats[userId] = [
      chats[userId][0], 
      ...chats[userId].slice(-10)
    ]
  }

  try {
    // إرسال سجل المحادثة بالكامل إلى خادم معالجة الذكاء الاصطناعي مع تحديد وقت أقصى للاستجابة بـ 20 ثانية
    const { data } = await axios.post('https://aichat-api.vercel.app/chatgpt', {
      messages: chats[userId]
    }, {
      timeout: 20000 
    })

    let res = data?.content?.trim() || 'عذراً، لم يتم إنشاء أي رد.'

    // إضافة إجابة الذكاء الاصطناعي إلى الذاكرة ليتذكرها في السؤال القادم
    chats[userId].push({
      role: "assistant",
      content: res
    })

    return res

  } catch (err) {
    return '⚠️ خادم الذكاء الاصطناعي لا يستجيب حالياً، يرجى إعادة المحاولة مرة أخرى.'
  }
}
