let handler = async (m, { conn }) => {
    // تحديد الرسالة المستهدفة (سواء كانت الرسالة المقتبسة أو الرسالة الحالية)
    let q = m.quoted ? m.quoted : m
    
    try {
        // 1. محاولة تحميل ملف الميديا (الصورة أو الفيديو) من رسالة العرض لمرة واحدة
        let media = await q.download?.()
        
        // جلب النص المصاحب للميديا إن وجد
        let caption = q.text || ''
        
        // 2. إعادة إرسال الملف إلى الدردشة كملف عادي قابل للحفظ مع قالب الرسائل fwc
        await conn.sendFile(m.chat, media, null, caption, m, null, fwc)
        
    } catch (e) {
        // 3. خطة بديلة وتنبيه باللغة العربية في حال لم تكن الرسالة المستهدفة "عرض لمرة واحدة" أو فشل تحميلها
        m.reply('✳️ عذراً، هذه الرسالة ليست رسالة عرض لمرة واحدة (View Once) أو تعذر استخراجها.')
    }
}

// إعدادات المساعدة والتصنيف للأمر بعد تعريبها لسهولة الوصول من المنيو العربي
handler.help = ['قراءة_المؤقت']
handler.tags = ['أدوات ومساعدة']

// مصفوفة الكلمات المفتاحية والاختصارات للأمر باللغتين العربية والإنجليزية
handler.command = ['readviewonce', 'read', 'ver', 'readvo', 'قراءة', 'افتري', 'شوف', 'فحص'] 

export default handler
