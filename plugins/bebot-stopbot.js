let handler = async (m, { conn }) => {
  let userBot = global.db.data.users[m.sender]
  
  // التحقق مما إذا كان الأمر يتم تنفيذه على البوت الرئيسي (Parent Bot)
  if (global.conn.user.jid === conn.user.jid) {
      // رفض الإيقاف وتوجيه المطور للتحكم من لوحة التحكم (الترمينال) لحماية البوت الأساسي
      await conn.reply(m.chat, '✳️ هذا الأمر مخصص لإيقاف البوتات الفرعية فقط، لإيقاف البوت الرئيسي يرجى التوجه إلى لوحة التحكم (Terminal).', m);
  } else {
      // إذا كان الأمر يتم تنفيذه داخل محادثة بوت فرعي، يتم تنفيذ إجراء الفصل الإجباري
      await conn.reply(m.chat, `✅ تم فصل اتصاله وإيقاف البوت الفرعي بنجاح.`, m);
    
      // إغلاق مقبس اتصال الويب (WebSocket) الخاص بهذا البوت الفرعي لقطع اتصاله بخوادم واتساب
      conn.ws.close();
  }
};

// إعدادات المساعدة والتصنيف للأمر بعد تعريبها
handler.help = ['إيقاف_البوت']
handler.tags = ['التبويت / البوتات الفرعية']
handler.command = ['إيقاف_البوت', 'اوقف_البوت', 'احذف_بوتى', 'stop', 'stopbot']

// تفعيل قيد المالك: لضمان أن صاحب البوت نفسه أو مطور النظام هو من يستطيع إيقافه
handler.owner = true

export default handler
