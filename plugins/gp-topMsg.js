let handler = async (m, { conn }) => {
    
    // 1. جلب كائن إحصائيات الرسائل من قاعدة البيانات الشاملة
    let stats = global.db.data.statsMsg || {}

    // التحقق الاستباقي: إذا لم تكن هناك أي بيانات أو تفاعلات مسجلة في هذا الجروب بعد
    if (!stats[m.chat]) return m.reply('📊 لا توجد بيانات تفاعل مسجلة في هذه المجموعة حتى الآن.')

    // تحويل كائن البيانات إلى مصفوفة (Array) لتسهيل عملية الفرز البرمجي
    let users = Object.entries(stats[m.chat])

    // 2. هندسة الفرز والتصفية: ترتيب الأعضاء تنازلياً بناءً على عدد الرسائل المرسلة
    let sorted = users.sort((a, b) => b[1] - a[1])

    // اقتطاع المراكز العشرة الأولى فقط لعرضهم في لوحة الصدارة
    let top = sorted.slice(0, 10)

    // صياغة واجهة التقرير ولوحة الصدارة باللغة العربية
    let text = '🏆 *قائمة الأعضاء الأكثر تفاعلاً بالرسائل (TOP 10)*\n\n'

    // 3. حلقة التكرار لصياغة القائمة المنسقة وحساب المراكز
    for (let i = 0; i < top.length; i++) {
        let user = top[i][0]   // معرف العضو (JID)
        let count = top[i][1]  // عدد رسائله المسجلة

        // دمج السطور وعرض رقم المركز مع تايج العضو وإجمالي رسائله
        text += `${i + 1}. @${user.split('@')[0]} ➭ *${count} رسالة*\n`
    }

    // 4. إرسال لوحة الصدارة النهائية للجروب وحقن مصفوفة المنشن لضمان تفعيل الإشارات
    conn.reply(m.chat, text, m, {
        mentions: top.map(v => v[0])
    })
}

// إعدادات المساعدة والتصنيف للأمر بعد تعريبها لسهولة الوصول من المنيو العربي
handler.help = ['الأكثر_تفاعلاً']
handler.tags = ['إشراف المجموعات']

// مصفوفة الكلمات المفتاحية والاختصارات للأمر باللغتين العربية والإنجليزية
handler.command = ['القروب_تفاعل', 'التفاعل', 'توب_رسائل', 'topmsg']

// قفل الأمان: تشغيل الأمر داخل المجموعات فقط لارتباطه بإحصائيات الجروبات
handler.group = true

export default handler
