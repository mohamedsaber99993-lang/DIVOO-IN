let handler = async (m, { text, usedPrefix, command }) => {
    // التأكد من تهيئة كائن الملصقات في قاعدة البيانات العالمية لتجنب الأخطاء البرمجية
    global.db.data.sticker = global.db.data.sticker || {}
    
    // التحقق مما إذا كان المطور قد قام بالرد على رسالة
    if (!m.quoted) throw `✳️ يرجى الرد (Reply) على الملصق المراد ربطه بالأمر.`
    
    // التحقق مما إذا كانت الرسالة المشار إليها هي ملصق بالفعل وتحتوي على بصمة رقمية
    if (!m.quoted.fileSha256) throw `⚠️ عذراً، يجب أن تقوم بالرد على "ملصق" (Sticker) فقط.`
    
    // التحقق مما إذا كان المطور قد كتب الأمر المراد ربطه بجانب أمر التفعيل
    if (!text) throw `✳️ يرجى كتابة الأمر المراد ربطه بالملصق. مثال:\n${usedPrefix + command} .المنيو`
    
    let sticker = global.db.data.sticker
    // استخراج البصمة الفريدة للملصق وتحويلها إلى صيغة base64 لتعمل كمفتاح فريد في قاعدة البيانات
    let hash = m.quoted.fileSha256.toString('base64')
    
    // التحقق مما إذا كان هذا الملصق مرتبطاً بأمر سابق وتم قفله وحمايته (Locked) من قِبل النظام
    if (sticker[hash] && sticker[hash].locked) throw '⚠️ لا تمتلك الصلاحية لتغيير الأمر المرتبط بهذا الملصق، لأنه محمي ومغلق برمجياً.'
    
    // حفظ بيانات الربط الجديدة داخل قاعدة البيانات
    sticker[hash] = {
        text, // الأمر النصي الذي سيتم تنفيذه تلقائياً
        mentionedJid: m.mentionedJid, // الاحتفاظ بأي إشارات (Mentions) تمت كتابتها في الأمر
        creator: m.sender, // تسجيل معرف المطور الذي قام بعملية الربط
        at: + new Date, // تسجيل الطابع الزمني لوقت الحفظ
        locked: false, // تعيين حالة القفل كـ "مفتوح" بشكل افتراضي للسماح بالتعديل لاحقاً
    }
    
    // إرسال رسالة تأكيد النجاح باللغة العربية
    m.reply(`✅ تم حفظ الأمر وربطه بالملصق بنجاح! بمجرد إرسال الملصق سيتم تنفيذ الأمر تلقائياً.`)
}

// إعدادات المساعدة والتصنيف للأمر بعد تعريبها
handler.help = ['ربط_امر_الملصق']
handler.tags = ['التحكم / الأوامر']
handler.command = ['ربط_امر_الملصق', 'ربط_ملصق', 'setcmd']

// تفعيل قيد المالك الصارم: لضمان أن مطور البوت الأساسي فقط هو من يستطيع تعديل قاعدة بيانات الأوامر
handler.rowner = true

export default handler
