import fg from 'fg-senna'

let handler = async (m, { conn, text }) => {
  
  // 1. تحديد النص المدخل (سواء كان مكتوباً مع الأمر أو مأخوذاً من الرسالة التي تم الرد عليها)
  let teks = text ? text : m.quoted && m.quoted.text ? m.quoted.text : ''
  
  // التحقق الاستباقي: إذا لم يتم تحديد اسم الأغنية
  if (!teks) throw `✳️ يرجى إدخال اسم الأغنية أو النص المراد البحث عنه.`
  
  try {
    // استدعاء مكتبة البحث لجلب بيانات الأغنية والكلمات
    let res = await fg.lyrics(teks);
    
    // 2. صياغة تقرير الكلمات والتفاصيل باللغة العربية
    let mes = `▢ *العنوان:* ${res.title}
▢ *الفنان/المغني:* ${res.artist}

▢ *الكلمات:*
${res.lyrics}`;

    // 3. إرسال الكلمات مدمجة بصورة الغلاف الرسمية للأغنية مع قالب الرسائل المتقدم (fwc)
    conn.sendFile(m.chat, res.image, 'img.png', mes, m, null, fwc);
    
  } catch (e) {
    // وضع تفاعل خطأ على الرسالة في حال لم يتم العثور على الأغنية
    m.react('❌')
  } 

}

// إعدادات المساعدة والتصنيف للأمر بعد تعريبها لسهولة الوصول من المنيو العربي
handler.help = ['كلمات <اسم الأغنية>']
handler.tags = ['أدوات ومساعدة']

// مصفوفة الكلمات المفتاحية والاختصارات للأمر باللغتين العربية والإنجليزية
handler.command = ['letra', 'lyrics', 'letras', 'كلمات', 'كلمات_اغنية'] 

export default handler
